#include <stdio.h>
#include <stdlib.h>
/**
 * op_add - add a with b
 * @a: a
 * @b: b
 *
 * Return: add a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub a with b
 * @a: a
 * @b: b
 *
 * Return: sub a + b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul a with b
 * @a: a
 * @b: b
 *
 * Return: mul a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - add with b
 * @a: a
 * @b: b
 *
 * Return: div a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod a with b
 * @a: a
 * @b: b
 *
 * Return: mod a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
